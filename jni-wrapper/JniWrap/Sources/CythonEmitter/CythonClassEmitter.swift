import Foundation
import JniWrapCore

/// Emits a `.pyx` Cython module per Java class.
///
/// Each module:
///   * cimports the JNI shims from `jni_core` (path configurable via
///     `Options.jniCoreImport`),
///   * caches `jclass` + `jmethodID` + `jfieldID` in module-level cdef
///     slots, lazily resolved on first use,
///   * declares `cdef class <SimpleName>(JavaObject)` exposing
///     constructors, methods, fields, and (for enums) constant accessors.
///
/// Limitations called out inline in generated code:
///   * For overloaded methods only the first signature is bound under
///     the canonical name; a TODO marks the others until a Cython
///     dispatcher lands.
///   * Java interfaces are emitted as marker classes — no
///     PythonJavaClass-style reverse callbacks yet.
///   * Unknown Java reference types are accepted as ``JavaObject`` and
///     returned as opaque ``JavaObject`` instances.
public struct CythonClassEmitter {

    public struct Options: Sendable {
        public var jniCoreImport: String
        public init(jniCoreImport: String = "jni_core") {
            self.jniCoreImport = jniCoreImport
        }
    }

    public init() {}

    /// Render `.pyx` source for one top-level class.
    public func renderPyx(_ cls: ClassNode,
                          options: Options = .init()) -> String {
        var src = CythonSource()
        emitHeader(into: &src, jniCoreImport: options.jniCoreImport)
        emitClass(cls, into: &src, isTopLevel: true)
        return src.render()
    }

    /// Render `.pxd` source for one top-level class. Declares the
    /// `cdef class` and every `cdef _<name>(...)` C-only entry point so
    /// other Cython modules can `cimport` and call them at C speed
    /// without going through Python dispatch.
    public func renderPxd(_ cls: ClassNode,
                          options: Options = .init()) -> String {
        var src = CythonSource()
        src.line("# cython: language_level=3")
        src.line("# Generated by jni-wrap cython — do not edit by hand.")
        src.line("from \(options.jniCoreImport)._core cimport JavaObject")
        src.line("from \(options.jniCoreImport).jni cimport (")
        src.line("    JNIEnv, jobject, jstring,")
        src.line("    jboolean, jbyte, jchar, jshort, jint, jlong, jfloat, jdouble,")
        src.line(")")
        src.line("")
        emitPxdClass(cls, into: &src)
        return src.render()
    }

    private func emitPxdClass(_ cls: ClassNode, into src: inout CythonSource) {
        let slots = assignSlots(cls)
        src.line("cdef class \(cls.cythonLocalName)(JavaObject):")
        let javaObjReserved: Set<String> = [
            "_handle", "_env", "_adopt_local", "_adopt_global"]
        var emittedCanonical = Set<String>()
        var any = false

        for (m, _, pyName, idx) in slots.methods {
            let typedParams = paramSignatureCTyped(m.parameters)
            let retKind = jniKind(returnDescriptor(m.jniDescriptor))
            let retDecl = cdefReturnDecl(retKind)
            let exceptClause = cdefExceptClause(retKind)

            // Emit canonical declaration (no suffix) once per method name.
            if idx == 0 && !emittedCanonical.contains(pyName) {
                emittedCanonical.insert(pyName)
                let cdefCandidate = "_" + pyName
                let cdefName = javaObjReserved.contains(cdefCandidate)
                    ? cdefCandidate + "_impl" : cdefCandidate
                if m.isStatic {
                    src.line("    @staticmethod")
                    let envParam = typedParams.isEmpty ? "JNIEnv* env" : "JNIEnv* env, \(typedParams)"
                    src.line("    cdef \(retDecl) \(cdefName)(\(envParam))\(exceptClause)")
                } else {
                    let allParams = typedParams.isEmpty ? "self, JNIEnv* env" : "self, JNIEnv* env, \(typedParams)"
                    src.line("    cdef \(retDecl) \(cdefName)(\(allParams))\(exceptClause)")
                }
                any = true
            }

            // Emit indexed declaration (pyName_N) for every overload.
            let indexedPyName = "\(pyName)_\(idx)"
            let indexedCdefCandidate = "_" + indexedPyName
            let indexedCdefName = javaObjReserved.contains(indexedCdefCandidate)
                ? indexedCdefCandidate + "_impl" : indexedCdefCandidate
            if m.isStatic {
                src.line("    @staticmethod")
                let envParam = typedParams.isEmpty ? "JNIEnv* env" : "JNIEnv* env, \(typedParams)"
                src.line("    cdef \(retDecl) \(indexedCdefName)(\(envParam))\(exceptClause)")
            } else {
                let allParams = typedParams.isEmpty ? "self, JNIEnv* env" : "self, JNIEnv* env, \(typedParams)"
                src.line("    cdef \(retDecl) \(indexedCdefName)(\(allParams))\(exceptClause)")
            }
            any = true
        }
        if !any {
            src.line("    pass")
        }
        // Nested cdef classes get hoisted to file scope alongside parent
        // (Cython forbids nested cdef class); declare them here so
        // cross-module cimports resolve.
        for nested in cls.nested {
            src.line("")
            emitPxdClass(nested, into: &src)
        }
    }

    // MARK: - Header

    private func emitHeader(into src: inout CythonSource,
                            jniCoreImport core: String) {
        src.line("# cython: language_level=3")
        src.line("# Generated by jni-wrap cython — do not edit by hand.")
        src.line("")
        src.line("from \(core)._core cimport (")
        src.line("    JavaObject, get_env, check_jni_exc,")
        src.line("    find_class_global, get_method_id, get_static_method_id,")
        src.line("    get_field_id, get_static_field_id, j_str, py_str,")
        src.line(")")
        src.line("from \(core)._dispatch cimport (")
        src.line("    jnicore_NewObjectA, jnicore_DeleteLocalRef, jnicore_NewGlobalRef,")
        src.line("    jnicore_CallVoidMethodA, jnicore_CallObjectMethodA,")
        src.line("    jnicore_CallBooleanMethodA, jnicore_CallByteMethodA,")
        src.line("    jnicore_CallCharMethodA, jnicore_CallShortMethodA,")
        src.line("    jnicore_CallIntMethodA, jnicore_CallLongMethodA,")
        src.line("    jnicore_CallFloatMethodA, jnicore_CallDoubleMethodA,")
        src.line("    jnicore_CallStaticVoidMethodA, jnicore_CallStaticObjectMethodA,")
        src.line("    jnicore_CallStaticBooleanMethodA, jnicore_CallStaticByteMethodA,")
        src.line("    jnicore_CallStaticCharMethodA, jnicore_CallStaticShortMethodA,")
        src.line("    jnicore_CallStaticIntMethodA, jnicore_CallStaticLongMethodA,")
        src.line("    jnicore_CallStaticFloatMethodA, jnicore_CallStaticDoubleMethodA,")
        src.line("    jnicore_GetObjectField, jnicore_SetObjectField,")
        src.line("    jnicore_GetBooleanField, jnicore_SetBooleanField,")
        src.line("    jnicore_GetByteField, jnicore_SetByteField,")
        src.line("    jnicore_GetCharField, jnicore_SetCharField,")
        src.line("    jnicore_GetShortField, jnicore_SetShortField,")
        src.line("    jnicore_GetIntField, jnicore_SetIntField,")
        src.line("    jnicore_GetLongField, jnicore_SetLongField,")
        src.line("    jnicore_GetFloatField, jnicore_SetFloatField,")
        src.line("    jnicore_GetDoubleField, jnicore_SetDoubleField,")
        src.line("    jnicore_GetStaticObjectField, jnicore_SetStaticObjectField,")
        src.line("    jnicore_GetStaticBooleanField, jnicore_SetStaticBooleanField,")
        src.line("    jnicore_GetStaticByteField, jnicore_SetStaticByteField,")
        src.line("    jnicore_GetStaticCharField, jnicore_SetStaticCharField,")
        src.line("    jnicore_GetStaticShortField, jnicore_SetStaticShortField,")
        src.line("    jnicore_GetStaticIntField, jnicore_SetStaticIntField,")
        src.line("    jnicore_GetStaticLongField, jnicore_SetStaticLongField,")
        src.line("    jnicore_GetStaticFloatField, jnicore_SetStaticFloatField,")
        src.line("    jnicore_GetStaticDoubleField, jnicore_SetStaticDoubleField,")
        src.line(")")
        src.line("from \(core).jni cimport (")
        src.line("    jclass, jobject, jmethodID, jfieldID, jvalue, JNIEnv,")
        src.line("    jboolean, jbyte, jchar, jshort, jint, jlong, jfloat, jdouble, jstring,")
        src.line(")")
        src.line("")
        src.line("include \"\(core)/conversions.pxi\"")
        src.line("")
    }

    // MARK: - Class

    private struct MemberSlots {
        var ctors: [(node: ConstructorNode, slot: String)]
        var methods: [(node: MethodNode, slot: String, pyName: String, overloadIndex: Int)]
        var fields: [(node: FieldNode, slot: String, pyName: String)]
    }

    private func assignSlots(_ cls: ClassNode) -> MemberSlots {
        // All slot identifiers live at module scope (Cython forbids nested
        // cdef classes), so they must be namespaced by the owning class to
        // avoid collisions when a file hosts multiple hoisted nested types.
        let ns = safeIdent(cls.cythonLocalName)
        var ctors: [(ConstructorNode, String)] = []
        for (i, c) in cls.constructors.enumerated() {
            ctors.append((c, "_ctor_\(ns)_\(i)"))
        }
        // Group methods by name to compute overload index + pick first as canonical.
        var byName: [String: [(Int, MethodNode)]] = [:]
        var order: [String] = []
        for (i, m) in cls.methods.enumerated() {
            if byName[m.name] == nil { order.append(m.name) }
            byName[m.name, default: []].append((i, m))
        }
        var methods: [(MethodNode, String, String, Int)] = []
        for name in order {
            for (j, (_, m)) in byName[name]!.enumerated() {
                let slot = "_m_\(ns)_\(safeIdent(name))_\(j)"
                methods.append((m, slot, safePyName(name), j))
            }
        }
        var fields: [(FieldNode, String, String)] = []
        // Deduplicate: some classes have the same field name appearing twice
        // (e.g. CREATOR on Parcelable subclasses that inherit and re-declare it).
        var seenFieldKeys = Set<String>()
        for f in cls.fields {
            let key = (f.isStatic ? "s:" : "i:") + f.name
            guard seenFieldKeys.insert(key).inserted else { continue }
            let slot = (f.isStatic ? "_sf_" : "_f_") + "\(ns)_" + safeIdent(f.name)
            fields.append((f, slot, safePyName(f.name)))
        }
        return MemberSlots(ctors: ctors, methods: methods, fields: fields)
    }

    private func emitClass(_ cls: ClassNode,
                           into src: inout CythonSource,
                           isTopLevel: Bool) {
        let slots = assignSlots(cls)
        let classVar = "_cls_" + safeIdent(cls.cythonLocalName)
        let ensureFn = "_ensure_ids_" + safeIdent(cls.cythonLocalName)

        // Module/class metadata. `__javaclass__` is a module-level marker;
        // only emit it for the top-level class so nested hoisted classes
        // don't silently overwrite it.
        if isTopLevel {
            src.line("__javaclass__ = \"\(cls.jniName)\"  # \(cls.fqcn)")
            src.line("")
        } else {
            src.line("# Nested class: \(cls.fqcn)  (JNI: \(cls.jniName))")
        }

        // ID slots.
        src.line("cdef jclass \(classVar) = NULL")
        for (_, slot) in slots.ctors {
            src.line("cdef jmethodID \(slot) = NULL")
        }
        for (_, slot, _, _) in slots.methods {
            src.line("cdef jmethodID \(slot) = NULL")
        }
        for (_, slot, _) in slots.fields {
            src.line("cdef jfieldID \(slot) = NULL")
        }
        // Enum constant field slots (in addition to declared fields).
        var enumSlots: [(name: String, slot: String)] = []
        if cls.kind == .enumType, let constants = cls.enumConstants {
            for ec in constants {
                let slot = "_sf_enum_\(safeIdent(cls.cythonLocalName))_" + safeIdent(ec.name)
                enumSlots.append((ec.name, slot))
                src.line("cdef jfieldID \(slot) = NULL")
            }
        }
        src.line("")

        // Lazy resolver.
        src.line("cdef int \(ensureFn)(JNIEnv* env) except -1:")
        src.line("    global \(classVar)" + globalsList(slots: slots, enumSlots: enumSlots))
        src.line("    if \(classVar) != NULL:")
        src.line("        return 0")
        src.line("    \(classVar) = find_class_global(env, b\"\(cls.jniName)\")")
        for (c, slot) in slots.ctors {
            src.line("    \(slot) = get_method_id(env, \(classVar), b\"<init>\", b\"\(c.jniDescriptor)\")")
        }
        for (m, slot, _, _) in slots.methods {
            let getter = m.isStatic ? "get_static_method_id" : "get_method_id"
            src.line("    \(slot) = \(getter)(env, \(classVar), b\"\(m.name)\", b\"\(m.jniDescriptor)\")")
        }
        for (f, slot, _) in slots.fields {
            let getter = f.isStatic ? "get_static_field_id" : "get_field_id"
            src.line("    \(slot) = \(getter)(env, \(classVar), b\"\(f.name)\", b\"\(f.jniDescriptor)\")")
        }
        // Enum constants are static fields of type L<enum-fqcn>;
        for (name, slot) in enumSlots {
            src.line("    \(slot) = get_static_field_id(env, \(classVar), b\"\(name)\", b\"L\(cls.jniName);\")")
        }
        src.line("    return 0")
        src.line("")

        // Class body.
        if cls.kind == .interfaceType {
            src.line("# NOTE: \(cls.simpleName) is a Java interface. Cython subclassing of")
            src.line("# Java interfaces (PythonJavaClass equivalent) is not yet implemented.")
            src.line("# This class is provided to access the interface's static members only.")
        }

        let bases = "JavaObject"
        src.line("cdef class \(cls.cythonLocalName)(\(bases)):")
        src.line("    \"\"\"\(cls.fqcn)\"\"\"")
        src.line("")

        // Constructors (only first ctor wired to __init__ for v1).
        let nonInterface = cls.kind != .interfaceType
        if nonInterface, let (firstCtor, firstSlot) = slots.ctors.first {
            if slots.ctors.count > 1 {
                src.line("    # TODO: \(slots.ctors.count) constructor overloads; only the first is wired.")
                for (j, (c, _)) in slots.ctors.enumerated() {
                    src.line("    #   overload[\(j)] = \(c.jniDescriptor)")
                }
            }
            emitConstructor(firstCtor, slot: firstSlot,
                            classVar: classVar, ensureFn: ensureFn,
                            into: &src)
            src.line("")
        } else if nonInterface && slots.ctors.isEmpty {
            // No ctor (unlikely except abstract). Default __init__ to raise.
            src.line("    def __init__(self, *args, **kwargs):")
            src.line("        raise TypeError(\"\(cls.simpleName) has no public constructor\")")
            src.line("")
        }

        // Enum constants — expose as @staticmethod factories so callers
        // can do `MyEnum.VALUE()`. (Java enums expose them as static
        // fields of the enum type; the field ID is resolved alongside.)
        if cls.kind == .enumType, let constants = cls.enumConstants {
            let cn = cls.cythonLocalName
            for ec in constants {
                let slot = "_sf_enum_\(safeIdent(cn))_" + safeIdent(ec.name)
                src.line("    # Enum constant \(cls.simpleName).\(ec.name)")
                src.line("    @staticmethod")
                src.line("    def \(safePyName(ec.name))():")
                src.line("        cdef JNIEnv* env = get_env()")
                src.line("        \(ensureFn)(env)")
                src.line("        cdef jobject obj = jnicore_GetStaticObjectField(env, \(classVar), \(slot))")
                src.line("        check_jni_exc(env)")
                src.line("        cdef \(cn) inst = \(cn).__new__(\(cn))")
                src.line("        inst._adopt_local(env, obj)")
                src.line("        return inst")
                src.line("")
            }
        }

        // Fields → property pairs (getter+setter).
        for (f, slot, pyName) in slots.fields {
            emitField(f, slot: slot, pyName: pyName,
                      classVar: classVar, ensureFn: ensureFn,
                      ownerType: cls.cythonLocalName,
                      into: &src)
            src.line("")
        }

        // Methods — emit canonical (no suffix, idx==0) then all indexed overloads.
        var emittedCanonical = Set<String>()
        for (m, slot, pyName, idx) in slots.methods {
            // Canonical: emitted once per name, for the first overload.
            if idx == 0 && !emittedCanonical.contains(pyName) {
                emittedCanonical.insert(pyName)
                emitMethod(m, slot: slot, pyName: pyName,
                           classVar: classVar, ensureFn: ensureFn,
                           into: &src)
                src.line("")
            }
            // Indexed: emit pyName_N for every overload.
            emitMethod(m, slot: slot, pyName: "\(pyName)_\(idx)",
                       classVar: classVar, ensureFn: ensureFn,
                       into: &src)
            src.line("")
        }

        // from_java_object — utility to wrap any plain JavaObject as this type.
        let cn = cls.cythonLocalName
        src.line("    @staticmethod")
        src.line("    def from_java_object(obj) -> object:")
        src.line("        \"\"\"Wrap a plain JavaObject as \(cn) (no JNI type check).\"\"\"")
        src.line("        cdef JNIEnv* env = get_env()")
        src.line("        cdef \(cn) inst = \(cn).__new__(\(cn))")
        src.line("        cdef jobject global_ref = jnicore_NewGlobalRef(env, (<JavaObject?>obj)._handle)")
        src.line("        inst._adopt_global(env, global_ref)")
        src.line("        return inst")
        src.line("")

        // Nested classes — render as siblings at file scope (Cython does
        // not allow nested cdef class). They keep simple names but their
        // jniName already encodes the `$` separator from the AST.
        for nested in cls.nested {
            emitClass(nested, into: &src, isTopLevel: false)
        }
        _ = isTopLevel
    }

    private func globalsList(slots: MemberSlots, enumSlots: [(name: String, slot: String)] = []) -> String {
        var names: [String] = []
        names.append(contentsOf: slots.ctors.map { $0.slot })
        names.append(contentsOf: slots.methods.map { $0.slot })
        names.append(contentsOf: slots.fields.map { $0.slot })
        names.append(contentsOf: enumSlots.map { $0.slot })
        return names.isEmpty ? "" : ", " + names.joined(separator: ", ")
    }

    // MARK: - Constructor / Method / Field bodies

    private func emitConstructor(_ c: ConstructorNode, slot: String,
                                 classVar: String, ensureFn: String,
                                 into src: inout CythonSource) {
        let params = paramSignature(c.parameters)
        src.line("    def __init__(self, \(params)):")
        src.line("        cdef JNIEnv* env = get_env()")
        src.line("        \(ensureFn)(env)")
        emitCallSetup(parameters: c.parameters, indent: "        ", into: &src)
        src.line("        cdef jobject _ret = jnicore_NewObjectA(env, \(classVar), \(slot), _args)")
        emitLocalRefReleases(parameters: c.parameters, indent: "        ", into: &src)
        src.line("        check_jni_exc(env)")
        src.line("        self._adopt_local(env, _ret)")
    }

    private func emitMethod(_ m: MethodNode, slot: String, pyName: String,
                            classVar: String, ensureFn: String,
                            into src: inout CythonSource) {
        // We emit *two* entry points per Java method so the wrapper is
        // usable from both worlds without either side paying the other's
        // tax:
        //
        //   `cdef _<name>(... raw JNI types ...) except *`
        //     — Cython-only fast path. Takes jobject/jstring/jboolean/…
        //       directly; no Python boxing, no JavaObject wrapping, no
        //       local-ref cleanup. The caller is responsible for ref
        //       management on the inputs. Returns the raw JNI value
        //       (or `void` / `bint` for primitives).
        //
        //   `def <name>(... Python types ...)`
        //     — Python-facing wrapper. Coerces Python args (str → jstring,
        //       JavaObject → jobject._handle, bool → jboolean, …),
        //       delegates to `_<name>`, wraps the result back into Python
        //       types (jobject → JavaObject, jstring → str), and releases
        //       any local refs it created on the way in.
        //
        // Other Cython modules `cimport` this class and call `obj._name(…)`
        // to get a direct C-level dispatch — zero PyObject* round-tripping.
        // Avoid shadowing JavaObject's own cdef data attributes (_handle, _env,
        // _adopt_local, _adopt_global). When the Java method name maps to one of
        // those, append "_impl" so the cdef entry point doesn't conflict.
        let cdefCandidate = "_" + pyName
        let javaObjReserved: Set<String> = [
            "_handle", "_env", "_adopt_local", "_adopt_global"]
        let cdefName = javaObjReserved.contains(cdefCandidate)
            ? cdefCandidate + "_impl" : cdefCandidate
        emitMethodCdef(m, slot: slot, pyName: pyName, cdefName: cdefName,
                       classVar: classVar, ensureFn: ensureFn, into: &src)
        src.line("")
        emitMethodPyWrapper(m, pyName: pyName, cdefName: cdefName,
                            classVar: classVar, into: &src)
    }

    /// Emits `cdef <retC> _<name>(self?, <C-typed params>) except *:`
    /// — the zero-Python-overhead Cython entry point.
    private func emitMethodCdef(_ m: MethodNode, slot: String,
                                pyName: String, cdefName: String,
                                classVar: String, ensureFn: String,
                                into src: inout CythonSource) {
        let typedParams = paramSignatureCTyped(m.parameters)
        let retKind = jniKind(returnDescriptor(m.jniDescriptor))
        let retDecl = cdefReturnDecl(retKind)
        let exceptClause = cdefExceptClause(retKind)
        if m.isStatic {
            src.line("    @staticmethod")
            let envParam = typedParams.isEmpty ? "JNIEnv* env" : "JNIEnv* env, \(typedParams)"
            src.line("    cdef \(retDecl) \(cdefName)(\(envParam))\(exceptClause):")
        } else {
            let allParams = typedParams.isEmpty ? "self, JNIEnv* env" : "self, JNIEnv* env, \(typedParams)"
            src.line("    cdef \(retDecl) \(cdefName)(\(allParams))\(exceptClause):")
        }
        src.line("        \(ensureFn)(env)")
        // C-typed args go straight into the jvalue slots — no conversion.
        emitCallSetupCTyped(parameters: m.parameters, indent: "        ", into: &src)
        emitCallSite(m: m, slot: slot, classVar: classVar,
                     indent: "        ", into: &src)
        src.line("        check_jni_exc(env)")
        // Return raw JNI value (caller owns it). No JavaObject wrapping.
        switch retKind {
        case .void:
            break
        case .jstring:
            src.line("        return <jstring>_ret")
        default:
            src.line("        return _ret")
        }
    }

    /// Emits `def <name>(self?, <Python-typed params>):` — the Python
    /// entry point. Converts inputs, calls the cdef variant, wraps
    /// outputs.
    private func emitMethodPyWrapper(_ m: MethodNode, pyName: String,
                                     cdefName: String, classVar: String,
                                     into src: inout CythonSource) {
        let pyParams = paramSignature(m.parameters)
        let pyReturn = pythonAnnotation(m.returnTypeFqcn)
        if m.isStatic {
            src.line("    @staticmethod")
            src.line("    def \(pyName)(\(pyParams)) -> \(pyReturn):")
        } else {
            let allParams = pyParams.isEmpty ? "self" : "self, \(pyParams)"
            src.line("    def \(pyName)(\(allParams)) -> \(pyReturn):")
        }
        src.line("        cdef JNIEnv* env = get_env()")
        let retKind = jniKind(returnDescriptor(m.jniDescriptor))
        // Pre-declare _ret so the assignment can live inside a try block
        // without triggering Cython's "cdef inside conditional" warnings.
        switch retKind {
        case .void:        break
        case .jboolean:    src.line("        cdef jboolean _ret = 0")
        case .jbyte:       src.line("        cdef jbyte _ret = 0")
        case .jchar:       src.line("        cdef jchar _ret = 0")
        case .jshort:      src.line("        cdef jshort _ret = 0")
        case .jint:        src.line("        cdef jint _ret = 0")
        case .jlong:       src.line("        cdef jlong _ret = 0")
        case .jfloat:      src.line("        cdef jfloat _ret = 0.0")
        case .jdouble:     src.line("        cdef jdouble _ret = 0.0")
        case .jstring:     src.line("        cdef jstring _ret = NULL")
        case .jobject, .jarray:
            src.line("        cdef jobject _ret = NULL")
        }
        // Coerce Python args → raw JNI types.
        let argNames = emitPyToJniCoercions(parameters: m.parameters,
                                            indent: "        ", into: &src)
        let callee = m.isStatic
            ? "\(currentClassName(classVar)).\(cdefName)"
            : "self.\(cdefName)"
        let argList = (["env"] + argNames).joined(separator: ", ")
        let hasStringArgs = m.parameters.contains { jniKind($0.jniDescriptor) == .jstring }
        let assign = (retKind == .void) ? "" : "_ret = "
        if hasStringArgs {
            src.line("        try:")
            src.line("            \(assign)\(callee)(\(argList))")
            src.line("        finally:")
            for (i, p) in m.parameters.enumerated()
            where jniKind(p.jniDescriptor) == .jstring {
                src.line("            if _arg\(i)_s != NULL:")
                src.line("                jnicore_DeleteLocalRef(env, <jobject>_arg\(i)_s)")
                _ = p
            }
        } else {
            src.line("        \(assign)\(callee)(\(argList))")
        }
        // Wrap raw return into Python.
        switch retKind {
        case .void:
            return
        case .jboolean:
            src.line("        return jboolean_to_py(_ret)")
        case .jbyte, .jchar, .jshort, .jint:
            src.line("        return <int>_ret")
        case .jlong:
            src.line("        return <long long>_ret")
        case .jfloat, .jdouble:
            src.line("        return <double>_ret")
        case .jstring:
            src.line("        return py_str(env, _ret)")
        case .jobject:
            src.line("        if _ret == NULL:")
            src.line("            return None")
            // If the return type is the same class we're emitting, wrap as that
            // concrete type so callers get a typed instance without a separate
            // from_java_object() call.
            var retSimple = m.returnTypeFqcn
                .components(separatedBy: ".").last ?? m.returnTypeFqcn
            retSimple = retSimple
                .components(separatedBy: "$").last ?? retSimple
            let wrapClass = retSimple == currentClassName(classVar)
                ? retSimple : "JavaObject"
            src.line("        cdef \(wrapClass) _wrapped = \(wrapClass).__new__(\(wrapClass))")
            src.line("        _wrapped._adopt_local(env, _ret)")
            src.line("        return _wrapped")
        case .jarray:
            src.line("        if _ret == NULL:")
            src.line("            return None")
            src.line("        cdef JavaObject _wrapped = JavaObject.__new__(JavaObject)")
            src.line("        _wrapped._adopt_local(env, _ret)")
            src.line("        return _wrapped")
        }
    }

    /// Emits `cdef jX _argN_s = j_str(env, name)` / `cdef jobject _hN = …`
    /// lines and returns the list of variable names to pass to `_<name>`.
    private func emitPyToJniCoercions(parameters: [ParamNode],
                                      indent: String,
                                      into src: inout CythonSource) -> [String] {
        var args: [String] = []
        // Pre-declare jstring locals first so they're reachable from finally.
        for (i, p) in parameters.enumerated()
        where jniKind(p.jniDescriptor) == .jstring {
            src.line("\(indent)cdef jstring _arg\(i)_s = NULL")
            _ = p
        }
        for (i, p) in parameters.enumerated() {
            let name = safePyName(p.name.isEmpty ? "arg\(i)" : p.name)
            let kind = jniKind(p.jniDescriptor)
            switch kind {
            case .void:
                break
            case .jboolean:
                src.line("\(indent)cdef jboolean _c\(i) = py_to_jboolean(\(name))")
                args.append("_c\(i)")
            case .jbyte:
                src.line("\(indent)cdef jbyte _c\(i) = py_to_jbyte(\(name))")
                args.append("_c\(i)")
            case .jchar:
                src.line("\(indent)cdef jchar _c\(i) = py_to_jchar(\(name))")
                args.append("_c\(i)")
            case .jshort:
                src.line("\(indent)cdef jshort _c\(i) = py_to_jshort(\(name))")
                args.append("_c\(i)")
            case .jint:
                src.line("\(indent)cdef jint _c\(i) = py_to_jint(\(name))")
                args.append("_c\(i)")
            case .jlong:
                src.line("\(indent)cdef jlong _c\(i) = py_to_jlong(\(name))")
                args.append("_c\(i)")
            case .jfloat:
                src.line("\(indent)cdef jfloat _c\(i) = py_to_jfloat(\(name))")
                args.append("_c\(i)")
            case .jdouble:
                src.line("\(indent)cdef jdouble _c\(i) = py_to_jdouble(\(name))")
                args.append("_c\(i)")
            case .jstring:
                src.line("\(indent)_arg\(i)_s = j_str(env, \(name)) if \(name) is not None else <jstring>NULL")
                args.append("_arg\(i)_s")
            case .jobject, .jarray:
                src.line("\(indent)cdef jobject _h\(i) = (<JavaObject?>\(name))._handle if \(name) is not None else <jobject>NULL")
                args.append("_h\(i)")
            }
        }
        return args
    }

    /// Fills `_args[i].<slot> = <variable>` from the *already C-typed*
    /// parameters of the `cdef _<name>` entry point.
    private func emitCallSetupCTyped(parameters: [ParamNode],
                                     indent: String,
                                     into src: inout CythonSource) {
        let n = parameters.count
        if n == 0 {
            src.line("\(indent)cdef jvalue _args[1]  # zero-arg call; _args ignored by JNI")
        } else {
            src.line("\(indent)cdef jvalue _args[\(n)]")
        }
        for (i, p) in parameters.enumerated() {
            let name = safePyName(p.name.isEmpty ? "arg\(i)" : p.name)
            let kind = jniKind(p.jniDescriptor)
            switch kind {
            case .void: break
            case .jboolean: src.line("\(indent)_args[\(i)].z = \(name)")
            case .jbyte:    src.line("\(indent)_args[\(i)].b = \(name)")
            case .jchar:    src.line("\(indent)_args[\(i)].c = \(name)")
            case .jshort:   src.line("\(indent)_args[\(i)].s = \(name)")
            case .jint:     src.line("\(indent)_args[\(i)].i = \(name)")
            case .jlong:    src.line("\(indent)_args[\(i)].j = \(name)")
            case .jfloat:   src.line("\(indent)_args[\(i)].f = \(name)")
            case .jdouble:  src.line("\(indent)_args[\(i)].d = \(name)")
            case .jstring:  src.line("\(indent)_args[\(i)].l = <jobject>\(name)")
            case .jobject, .jarray:
                src.line("\(indent)_args[\(i)].l = \(name)")
            }
        }
    }

