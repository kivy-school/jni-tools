            src.line("\(indent)if _ret == NULL:")
            src.line("\(indent)    return None")
            src.line("\(indent)cdef JavaObject _wrapped = JavaObject.__new__(JavaObject)")
            src.line("\(indent)_wrapped._adopt_local(env, _ret)")
            src.line("\(indent)return _wrapped")
        }
    }

