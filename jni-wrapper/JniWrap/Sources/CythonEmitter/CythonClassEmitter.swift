    private func emitReturn(m: MethodNode, indent: String,
                            into src: inout CythonSource) {
        let ret = jniKind(m.jniDescriptor.split(separator: ")").last.map(String.init) ?? "V")
        switch ret {
        case .void:
            return
        case .jboolean:
            src.line("\(indent)return jboolean_to_py(_ret)")
        case .jbyte, .jchar, .jshort, .jint:
            src.line("\(indent)return <int>_ret")
        case .jlong:
            src.line("\(indent)return <long long>_ret")
        case .jfloat, .jdouble:
            src.line("\(indent)return <double>_ret")
        case .jstring:
            src.line("\(indent)return py_str(env, _ret)")
        case .jobject, .jarray:
            // Wrap opaque jobject as a JavaObject for now. Type-aware
            // wrapping (e.g. resolving back into the wrapper class) is
