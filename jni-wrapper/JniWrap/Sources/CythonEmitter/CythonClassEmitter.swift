        case .jboolean:
            src.line("\(indent)cdef jboolean _ret = \(prefix)BooleanMethodA(env, \(target), \(slot), _args)")
        case .jbyte:
            src.line("\(indent)cdef jbyte _ret = \(prefix)ByteMethodA(env, \(target), \(slot), _args)")
        case .jchar:
