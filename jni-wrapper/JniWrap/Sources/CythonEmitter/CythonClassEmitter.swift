    /// Strip the `_cls_` prefix to recover the cdef-class type name —
    /// used to spell static-method calls as `ClassName._foo(...)`.
    private func currentClassName(_ classVar: String) -> String {
        if classVar.hasPrefix("_cls_") {
            return String(classVar.dropFirst("_cls_".count))
        }
        return classVar
    }

    private func emitField(_ f: FieldNode, slot: String, pyName: String,
                           classVar: String, ensureFn: String,
                           ownerType: String,
                           into src: inout CythonSource) {
        let pyType = pythonAnnotation(f.typeFqcn)
        let kind = jniKind(f.jniDescriptor)
        let getCall: String
        let setCall: (String) -> String
        if f.isStatic {
            getCall = staticFieldGetter(kind) + "(env, \(classVar), \(slot))"
            setCall = { v in self.staticFieldSetter(kind) + "(env, \(classVar), \(slot), \(v))" }
        } else {
            getCall = instanceFieldGetter(kind) + "(env, self._handle, \(slot))"
            setCall = { v in self.instanceFieldSetter(kind) + "(env, self._handle, \(slot), \(v))" }
        }

        // @property getter
        let pyGetSelf = f.isStatic ? "()" : "(self)"
        if f.isStatic {
            src.line("    # Static field \(ownerType).\(f.name)")
            src.line("    @staticmethod")
        } else {
            src.line("    @property")
        }
        src.line("    def \(pyName)\(pyGetSelf) -> \(pyType):")
        src.line("        cdef JNIEnv* env = get_env()")
        src.line("        \(ensureFn)(env)")
        emitFieldGet(kind: kind, getCall: getCall, returnFqcn: f.typeFqcn,
                     indent: "        ", into: &src)

        // Setter — skip for final fields and for static fields (Python
        // can't bind a @<name>.setter to a @staticmethod-style accessor
        // cleanly; static field writes are uncommon enough to defer).
        if !f.isStatic && !f.modifiers.contains(.final) {
            src.line("")
            src.line("    @\(pyName).setter")
            src.line("    def \(pyName)(self, value) -> None:")
            src.line("        cdef JNIEnv* env = get_env()")
            src.line("        \(ensureFn)(env)")
            emitFieldSet(kind: kind, setBuilder: setCall, paramFqcn: f.typeFqcn,
                         indent: "        ", into: &src)
        }
    }

