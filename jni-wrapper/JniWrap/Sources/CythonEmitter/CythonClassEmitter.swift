    /// `except` clause for the cdef entry point. Object/string returns use
    /// `except? NULL` (NULL is a legal Java null, so we mark it ambiguous).
    /// Numeric returns use `except *` to force a PyErr_Occurred check after
    /// every call (JNI exceptions surface via `check_jni_exc`).
    private func cdefExceptClause(_ k: JniKind) -> String {
        switch k {
        case .void:
            return " except *"
        case .jstring, .jobject, .jarray:
            return " except? NULL"
        default:
            return " except *"
        }
    }

