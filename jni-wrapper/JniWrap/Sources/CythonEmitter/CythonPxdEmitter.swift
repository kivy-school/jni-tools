import Foundation
import JniWrapCore

/// Emits a `.pxd` declaring the cdef class so OTHER Cython modules can
/// `cimport` it. Mirrors the structure of the matching `.pyx` but only
/// declares public cdef attributes — Python-visible `def` methods do not
/// belong in a `.pxd`.
public struct CythonPxdEmitter {
    public init() {}

    public func render(_ cls: ClassNode) -> String {
        var src = CythonSource()
        src.line("# cython: language_level=3")
        src.line("# Generated by jni-wrap cython — do not edit by hand.")
        src.line("from jni_core._core cimport JavaObject")
        src.line("")
        renderClass(cls, into: &src)
        return src.render()
    }

    private func renderClass(_ cls: ClassNode, into src: inout CythonSource) {
        src.line("cdef class \(cls.cythonLocalName)(JavaObject):")
        // No extra cdef storage on the subclass — all state lives on
        // JavaObject (`_handle`, `_env`). Keep the body minimal so the
        // declaration matches the .pyx's `cdef class` line exactly.
        src.line("    pass")
        // Nested cdef classes get hoisted to file scope alongside their
        // parent (Cython forbids nested cdef classes); declare them here
        // so cross-module cimports resolve.
        for nested in cls.nested {
            src.line("")
            renderClass(nested, into: &src)
        }
    }
}
