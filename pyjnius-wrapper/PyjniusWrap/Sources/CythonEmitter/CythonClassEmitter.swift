    // MARK: - Signatures + naming

    private func paramSignature(_ params: [ParamNode]) -> String {
        var out: [String] = []
        for (i, p) in params.enumerated() {
            let name = safePyName(p.name.isEmpty ? "arg\(i)" : p.name)
            out.append(name)
        }
        return out.joined(separator: ", ")
    }

    /// Pythonic annotation for the `.pyx` signature. We keep these
    /// extremely loose (no imports inside .pyx) so the file compiles
    /// standalone — the rich annotation lives in the companion `.pyi`.
    private func pythonAnnotation(_ fqcn: String) -> String {
        if fqcn.hasSuffix("[]") { return "list" }
        switch fqcn {
        case "void": return "None"
        case "boolean": return "bool"
        case "byte", "short", "int", "long": return "int"
        case "float", "double": return "float"
        case "char": return "str"
        case "java.lang.String", "java.lang.CharSequence": return "str"
        default: return "object"
        }
    }

    /// JNI parameter/return slot classification used to pick the right
    /// jvalue field, Call*Method variant, and conversion shim.
    enum JniKind {
        case void, jboolean, jbyte, jchar, jshort, jint, jlong, jfloat, jdouble
        case jstring, jobject, jarray
    }

    /// First char of a JNI descriptor → kind. Special-cases the canonical
    /// String descriptor since we have a dedicated jstring shim.
    func jniKind(_ desc: String) -> JniKind {
        if desc == "Ljava/lang/String;" || desc == "Ljava/lang/CharSequence;" {
            return .jstring
        }
        guard let c = desc.first else { return .void }
        switch c {
        case "V": return .void
        case "Z": return .jboolean
        case "B": return .jbyte
        case "C": return .jchar
        case "S": return .jshort
        case "I": return .jint
        case "J": return .jlong
        case "F": return .jfloat
        case "D": return .jdouble
        case "L": return .jobject
        case "[": return .jarray
        default:  return .jobject
        }
    }

    // MARK: - Identifier hygiene

    private func safeIdent(_ s: String) -> String {
        // Replace non-identifier chars (e.g. `$` from inner classes) with `_`.
        var out = ""
        for ch in s {
            if ch.isLetter || ch.isNumber || ch == "_" { out.append(ch) }
            else { out.append("_") }
        }
        if let first = out.first, first.isNumber { out = "_" + out }
        if out.isEmpty { out = "_" }
        return out
    }

    private func safePyName(_ s: String) -> String {
        Self.pyKeywords.contains(s) ? s + "_" : s
    }

    private static let pyKeywords: Set<String> = [
        "False","None","True","and","as","assert","async","await","break","class",
        "continue","def","del","elif","else","except","finally","for","from","global",
        "if","import","in","is","lambda","nonlocal","not","or","pass","raise","return",
        "try","while","with","yield","match","case"
    ]
